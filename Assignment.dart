void main() {
  // Integer data type
  int age = 29;

  // Double data type
  double height = 5.9;

  // String data type
  String name = "Musa";

  // String data type
  String surName = "Motloung";

  // Boolean data type
  bool isStudent = true;

  // List data type
  List<String> hobbies = ["reading", "farming", "coding"];

  // Map data type
  Map<String, dynamic> person = {
    "name": "T.L.O.U",
    "age": 2,
    "isStudent": false,
    "hobbies": ["running", "coding"]
  };

  // Printing values
  print("Name: $name");
  print("Surname: $surName");
  print("Age: $age");
  print("Height: $height");
  print("Is Student: $isStudent");
  print("Hobbies: $hobbies");
  print("Person: $person");
}
